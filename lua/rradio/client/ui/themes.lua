local function resolveThemeName( themeName, options )
    themeName = tostring( themeName or "" )
    options = type( options ) == "table" and options or {}
    if themes.List[themeName] and ( options.allowExclusive == true or not EXCLUSIVE_THEMES[themeName] ) then
        return themeName
    end

    return DEFAULT_THEME
end

function themes.Apply( themeName, options )
    options = type( options ) == "table" and options or {}
    local resolvedName = resolveThemeName( themeName, options )
    local theme = themes.List[resolvedName] or themes.List[DEFAULT_THEME]

    rRadio.config.UI = table.Copy( theme )
    appliedThemeName = resolvedName
    hook.Run( "rRadio_ThemeChanged", resolvedName, rRadio.config.UI, options )

    return resolvedName
end

function themes.GetAppliedName()
    return appliedThemeName
end

function themes.ResolveUserThemeName( themeName )
    return resolveThemeName( themeName )
end

function themes.GetNames( options )
    options = type( options ) == "table" and options or {}

    local names = {}
    for themeName in pairs( themes.List ) do
        if options.includeExclusive == true or not EXCLUSIVE_THEMES[themeName] then
            names[#names + 1] = themeName
        end
    end

    table.sort( names )
    return names
end

function themes.Init()
    themes.Apply( GetConVar( "rammel_rradio_menu_theme" ):GetString() )

    cvars.AddChangeCallback( "rammel_rradio_menu_theme", function( _name, _oldValue, newValue )
        themes.Apply( newValue )
    end, "rRadio_UI_ThemeChanged" )
end

return themes
